function mouseOver() {
    document.getElementById("demo").style.color = "red";
    document.getElementById("demo").style.fontWeight = "bold";
    var style = document.getElementById("no1").style.display;
    document.getElementById("no1").style.display = "block";
    var style = document.getElementById("no1jua").style.display;
    document.getElementById("no1jua").style.display = "block";
}
function mouseOut() {
    document.getElementById("demo").style.color = "rgb(85, 83, 83)";
    document.getElementById("demo").style.fontWeight = "";
    document.getElementById("no1").style.display = "none";
    document.getElementById("no1jua").style.display = "none";
}
